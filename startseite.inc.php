<h2>Was ist der Food Truck?</h2>
<p>Der Food Truck bringt das Essen zu euch! Egal wo ihr arbeitet. Wir fahren jede Woche eine Tour ab um möglichst viele hungrige Angestellte in den Genuss unseres frischen Essens kommen zu lassen.
	Im Gegensatz zu Fast Food bietet der Food Truck eine ausgewogene Ernährung für alle Ansprüche. Da unseren (vorwiegend) berufstätigen Kunden nicht viel Zeit in ihrer Mittagspause bleibt stehen wir mit unserem Truck fast direkt vor eurer Firmentür für euch bereit.
</p>
