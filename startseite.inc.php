<h2>Weitere Infos</h2>
<p>Die Food Trucks sind ein Trend der aus den USA kommt. Dort wurde begonnen aus umgebauten LKW's und Kleintransportern aus Essen direkt auf der Straße zu verkaufen. Seit einigen Jahren fahren bereits die ersten Food Trucks in vielen deutschen Regionen herum um die zeitlich immer eingeschränkteren Mitarbeiter vielen Firmen mit gesundem Essen zu versorgen.<br>Alle paar Monate findet ein sogenannten Streetfood Festival statt, bei dem sich viele der Trucks treffen um Ideen auszutauschen und ihr Essen neuen Kundengruppe vorstellen können. Wenn ihr weitere Infos rund um das Thema Food Truck haben wollt, dann besucht die Seite <a href="http://www.foodtrucks-deutschland.de/">Food Trucks Deutschland</a>. Dort findet ihr alles rund ums Thema Food Trucks in Deutschland.</p>
